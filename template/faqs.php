        <h2>FAQs</h2>
        <h2>What are the rules?</h2>
          <p>We currently adhere to USAT Rules for Triathlon and Duathlon.  Important rules include no drafting, you must wear a helmet and music is ABSOLUTELY NOT allowed during the bike for obvious safety reasons.<br> <br>
        <h2>Can I use a personal music device while cycling?</h2>
        <p>Absolutely no music devices are allowed during the bike segment for obvious safety risks and will result in immediate disqualification.</p>
        <h2>Can I use a personal music device while running?</h2>
          <p>During any running segments, we prefer for athletes to NOT use music devices to maximize safety, assure a fair competitive environment and assure athletes can hear all course marshal instructions. With that said, we also understand that many athletes rely on music to help endure the challenge of running. To accommodate the needs of our athletes while still assuring maximum safety and a fair, competitive environment, here is the arrangement that we can accommodate.<br> <br>
             If you chose to use a personal music device, you must always keep one ear open for instructions so only one ear-bud can be used at any time. In addition, if you opt to use music, you will not be eligible for awards, points and rankings. So as an athlete, you have to make a choice music or awards/points/rankings.<br> <br>
             If you are using a personal music devise with two earbuds, you will be immediately disqualified. Trust us, we don't want to have to enforce a disqualification but in order to assure the safety of all athletes, this is a very important rule.
             We also request that if you chose to use a personal music device that you keep the volume to a minimum to assure all instructions can be heard.</p><br>
        <h2>Do I need to wear a wetsuit?</h2>
        <p>No, you do not need to wear a wetsuit. Many will not wear a wetsuit while others will choose to wear a wetsuit because of the buoyancy and warmth factor.</p><br>
        <h2>Do I have to use a road or racing bike?</h2>
           <p>No. We welcome any type of bike as long as it is functioning properly with brakes and endcaps at the end of your handlebars.</p><br>
